function calculateStats(data){
    var AVG;
    var OBP;
    var SLG;
    var OPS;
    var currentStats = data;

    if (currentStats[1] == 0){
        AVG = 0.000;
        OBP = (currentStats[3]+currentStats[10]+currentStats[9])/(currentStats[1]+currentStats[10]+currentStats[9]);
        SLG = 0.000;
        OPS = OBP + SLG;
    }
    else{
    AVG = (currentStats[3]/currentStats[1]);
    OBP = (currentStats[3]+currentStats[10]+currentStats[9])/(currentStats[1]+currentStats[10]+currentStats[9]);
    SLG = (currentStats[4]+(currentStats[5]*2)+(currentStats[6]*3)+(currentStats[7]*4))/currentStats[1];
    OPS = OBP + SLG
    }
    var fullStats = [AVG.toFixed(3),OBP.toFixed(3),SLG.toFixed(3),OPS.toFixed(3)];

    return fullStats


}


